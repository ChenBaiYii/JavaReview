public class PrimitiveDataType {
    public static void main(String[] args) {

        int a = 15;
        int b = 015;
        int c = 0x15;
        int d = 0b11001;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        byte age = 30;
        short salary = 30000;
        long globalPopulation = 7400000000L;
    }
}
